using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class GameTimeSwitcher : MonoBehaviour
{
    public static GameTimeSwitcher instance;

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
        }
        else if (instance == this)
        {
            Destroy(gameObject);
        }

        DontDestroyOnLoad(gameObject);
    }

    public void Pause()
    {
        Time.timeScale = 0f;
    }

    public void Resume()
    {
        Time.timeScale = 1f;
    }
}
